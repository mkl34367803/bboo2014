package com.smart.oo.domain.res;

public class Lines {

	private String depc;
	private String arrc;
	private String depCh;
	private String arrCh;
	private String fno;
	private String cabin;
	private String depd;
	private String dept;
	private String arrt;
	private String state;

	public String getDepc() {
		return depc;
	}

	public void setDepc(String depc) {
		this.depc = depc;
	}

	public String getArrc() {
		return arrc;
	}

	public void setArrc(String arrc) {
		this.arrc = arrc;
	}

	public String getFno() {
		return fno;
	}

	public void setFno(String fno) {
		this.fno = fno;
	}

	public String getCabin() {
		return cabin;
	}

	public void setCabin(String cabin) {
		this.cabin = cabin;
	}

	public String getDepd() {
		return depd;
	}

	public void setDepd(String depd) {
		this.depd = depd;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	public String getArrt() {
		return arrt;
	}

	public void setArrt(String arrt) {
		this.arrt = arrt;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getDepCh() {
		return depCh;
	}

	public void setDepCh(String depCh) {
		this.depCh = depCh;
	}

	public String getArrCh() {
		return arrCh;
	}

	public void setArrCh(String arrCh) {
		this.arrCh = arrCh;
	}

}
